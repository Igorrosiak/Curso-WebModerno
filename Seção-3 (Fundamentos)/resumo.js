// typeof mostra qual o tipo da variavel

/* NÚMEROS */
let idade = 31;
console.log(typeof idade)

let salario = 4578.32;
console.log(typeof salario)

/* BOOLEAN */
let estaChovendo = true
console.log(typeof estaChovendo)

/* STRING */
let texto = "Teste"
console.log(typeof texto)

// const é utilizado no lugar de let e var, para quando o valor não puder mudar após sua inserção 
const a = 25

/* constantes normalmente são "apelidadas" com letras maiúsculas */